/**
 * This class is for car objects.
 * Car data includes year and model
 * @author Brandon Ong
 * 
 */

public class Car{
	int year;
	String model;

	/**
	 * Constructor for car class.
	 * Initializes year and model
	 *
	 * @param year Year of the car
	 * @param model model name of car
	 */
	public Car(int year, String model){
		this.year = year;
		this.model = model;
	}

}
